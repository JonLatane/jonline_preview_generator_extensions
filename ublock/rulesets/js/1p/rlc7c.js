/******************************************************************************/

// Important!
// Isolate from global scope
(function() {

/******************************************************************************/

// isl-0

const argsMap = new Map([[607589139,{"a":"#banner,\n[href^=\"/is/moya/adverts/\"],\n[id^=\"box_aitem\"],\naside"}],[309279839,{"a":".col-md-8.ad.hausad"}],[1267538087,{"a":"#banner1,\n#banner10,\n#banner11,\n#banner12,\n#banner13,\n#banner14,\n#banner15,\n#banner16,\n#banner17,\n#banner2,\n#banner23,\n#banner3,\n#banner4,\n#banner5,\n#banner6,\n#banner7,\n#banner8,\n#banner9"}]]);

const hostnamesMap = new Map([["akureyri.net",607589139],["eyjar.net",309279839],["fotbolti.net",1267538087]]);

/******************************************************************************/

let hn;
try { hn = document.location.hostname; } catch(ex) { }
const styles = [];
while ( hn ) {
    if ( hostnamesMap.has(hn) ) {
        let argsHashes = hostnamesMap.get(hn);
        if ( typeof argsHashes === 'number' ) { argsHashes = [ argsHashes ]; }
        for ( const argsHash of argsHashes ) {
            const details = argsMap.get(argsHash);
            if ( details.n && details.n.includes(hn) ) { continue; }
            styles.push(details.a);
        }
    }
    if ( hn === '*' ) { break; }
    const pos = hn.indexOf('.');
    if ( pos !== -1 ) {
        hn = hn.slice(pos + 1);
    } else {
        hn = '*';
    }
}

if ( styles.length === 0 ) { return; }

try {
    const sheet = new CSSStyleSheet();
    sheet.replace(`@layer{${styles.join(',')}{display:none!important;}}`);
    document.adoptedStyleSheets = [
        ...document.adoptedStyleSheets,
        sheet
    ];
} catch(ex) {
}

/******************************************************************************/

argsMap.clear();
hostnamesMap.clear();

/******************************************************************************/

})();

