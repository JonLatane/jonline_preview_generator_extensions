// Important!
// Isolate from global scope
(function() {

/******************************************************************************/

// isl-0

const argsMap = new Map([[2123139039,{"a":"#sambio-banner,\n#top-banner,\n.ad-tower,\nDIV[style=\"padding: 20px 0; text-align: center;\"]"}],[639356180,{"a":".ad_310.add_marg_20_b"}],[1745505907,{"a":".add_top_border.left_floater,\n.head_ad_360x100,\n.left_floater.add_top_border,\n.top_customer_banner,\nIMG[src=\"/thumb/550x200/53d278ef882dd.jpg\"]"}],[984459500,{"a":"#ad-overlay-container"}]]);

const hostnamesMap = new Map([["sporttv.is",2123139039],["spyr.is",[639356180,1745505907]],["stundin.is",984459500]]);

/******************************************************************************/

let hn;
try { hn = document.location.hostname; } catch(ex) { }
const styles = [];
while ( hn ) {
    if ( hostnamesMap.has(hn) ) {
        let argsHashes = hostnamesMap.get(hn);
        if ( typeof argsHashes === 'number' ) { argsHashes = [ argsHashes ]; }
        for ( const argsHash of argsHashes ) {
            const details = argsMap.get(argsHash);
            if ( details.n && details.n.includes(hn) ) { continue; }
            styles.push(details.a);
        }
    }
    if ( hn === '*' ) { break; }
    const pos = hn.indexOf('.');
    if ( pos !== -1 ) {
        hn = hn.slice(pos + 1);
    } else {
        hn = '*';
    }
}

if ( styles.length === 0 ) { return; }

try {
    const sheet = new CSSStyleSheet();
    sheet.replace(`@layer{${styles.join(',')}{display:none!important;}}`);
    document.adoptedStyleSheets = [
        ...document.adoptedStyleSheets,
        sheet
    ];
} catch(ex) {
}

/******************************************************************************/

argsMap.clear();
hostnamesMap.clear();

/******************************************************************************/

})();

